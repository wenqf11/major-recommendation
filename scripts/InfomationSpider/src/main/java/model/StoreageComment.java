package model;

public class StoreageComment {

	public int commentId;
	public int schoolId;
	public int majorId;
	public double comprehensiveScore;
	public double teachingScore;
	public double dealScore;
	public double workScore;
	public int getCommentId() {
		return commentId;
	}
	public void setCommentId(int commentId) {
		this.commentId = commentId;
	}
	public int getSchoolId() {
		return schoolId;
	}
	public void setSchoolId(int schoolId) {
		this.schoolId = schoolId;
	}
	public int getMajorId() {
		return majorId;
	}
	public void setMajorId(int majorId) {
		this.majorId = majorId;
	}
	public double getComprehensiveScore() {
		return comprehensiveScore;
	}
	public void setComprehensiveScore(double comprehensiveScore) {
		this.comprehensiveScore = comprehensiveScore;
	}
	public double getTeachingScore() {
		return teachingScore;
	}
	public void setTeachingScore(double teachingScore) {
		this.teachingScore = teachingScore;
	}
	public double getDealScore() {
		return dealScore;
	}
	public void setDealScore(double dealScore) {
		this.dealScore = dealScore;
	}
	public double getWorkScore() {
		return workScore;
	}
	public void setWorkScore(double workScore) {
		this.workScore = workScore;
	}
	
	
}
